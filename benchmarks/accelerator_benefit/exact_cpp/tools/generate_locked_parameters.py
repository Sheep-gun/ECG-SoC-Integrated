#!/usr/bin/env python3
"""Generate immutable C++ constants from the locked RTL/config sources."""

from __future__ import annotations

import hashlib
import json
import re
import shutil
import subprocess
import sys
from pathlib import Path


EXACT = Path(__file__).resolve().parents[1]
REPO = EXACT.parents[2]
LOCKED_COMMIT = "c6b80de19cdcad5b7e43fe7835588b629d847f75"
MODEL_ID = "structural_guarded_silent_aff_1008710"
PARAM_HASH = "7a4383441d6a6b2c9d88dba253ca6809f424ce36ca0a09a2876dac3696d33c1b"
CLASSES = ("NSR", "CHF", "ARR", "AFF")
GIT = shutil.which("git") or r"C:\Users\YangGeon\.cache\codex-runtimes\codex-primary-runtime\dependencies\native\git\cmd\git.exe"


def git(*args: str, capture: bool = False) -> bytes:
    return subprocess.check_output([GIT, *args], cwd=REPO) if capture else subprocess.check_call([GIT, *args], cwd=REPO)  # type: ignore[return-value]


def blob_sha(path: str) -> str:
    return hashlib.sha256(git("cat-file", "blob", f"HEAD:{path}", capture=True)).hexdigest()


def main() -> int:
    locked_paths = [
        "rtl/core/class_score_neurons.v",
        "rtl/strict_recordwise_locked_params.vh",
        "rtl/final_membrane_layer.v",
        "rtl/snn_ecg_30min_final_top.v",
        "configs/final_submission_locked_model.json",
        "configs/recordwise_resplit_seed20260808/best_final_membrane_structural_grid_locked.json",
    ]
    git("diff", "--quiet", LOCKED_COMMIT, "HEAD", "--", *locked_paths)

    config = json.loads((REPO / locked_paths[4]).read_text(encoding="utf-8-sig"))
    structural = json.loads((REPO / locked_paths[5]).read_text(encoding="utf-8-sig"))
    if config["final_model_id"] != MODEL_ID or structural["selected_candidate_id"] != MODEL_ID:
        raise SystemExit("locked model ID mismatch")
    if config["locked_params_hash"] != PARAM_HASH or structural["selected_candidate_params_hash"] != PARAM_HASH:
        raise SystemExit("locked parameter payload hash mismatch")

    sys.path.insert(0, str(REPO / "benchmarks" / "accelerator_benefit" / "tools"))
    import snapshot_c24_rtl_exact as reference  # pylint: disable=import-error,import-outside-toplevel

    constants = dict(reference._CLASS_CONSTS)  # verification-only parser output
    header_text = (REPO / "rtl" / "strict_recordwise_locked_params.vh").read_text(encoding="utf-8")
    final_constants = {
        name: int(value)
        for name, value in re.findall(
            r"localparam\s+integer\s+(\w+)\s*=\s*(-?\d+)\s*;", header_text
        )
    }

    grouped: dict[str, list[int]] = {}
    for name in constants:
        for cls in CLASSES:
            suffix = "_" + cls
            if name.endswith(suffix):
                base = name[: -len(suffix)]
                if all(f"{base}_{candidate}" in constants for candidate in CLASSES):
                    grouped[base] = [constants[f"{base}_{candidate}"] for candidate in CLASSES]
                break

    lines = [
        "#pragma once",
        "",
        "// Generated by tools/generate_locked_parameters.py; do not hand edit.",
        "#include <array>",
        "#include <cstdint>",
        "",
        "namespace snn::params {",
        f'constexpr char model_id[] = "{MODEL_ID}";',
        f'constexpr char parameter_payload_sha256[] = "{PARAM_HASH}";',
        f'constexpr char locked_commit[] = "{LOCKED_COMMIT}";',
    ]
    for path in locked_paths:
        ident = re.sub(r"[^A-Za-z0-9]", "_", path).strip("_")
        lines.append(f'constexpr char sha256_{ident}[] = "{blob_sha(path)}";')
    lines.append("")
    for name, value in sorted(constants.items()):
        lines.append(f"constexpr std::int64_t {name} = {value}LL;")
    lines.append("")
    for name, values in sorted(grouped.items()):
        joined = ", ".join(f"{value}LL" for value in values)
        lines.append(f"constexpr std::array<std::int64_t, 4> {name} = {{{{{joined}}}}};")
    lines.extend(["", "constexpr std::int64_t C24_RDM_LEVEL[4][15] = {"])
    for cls in CLASSES:
        lines.append("    {" + ", ".join(str(v) + "LL" for v in reference._C24_RDM_LEVEL[cls]) + "},")
    lines.extend(["};", "constexpr std::int64_t LOCAL_RDM_GE[4][15] = {"])
    for cls in CLASSES:
        lines.append("    {" + ", ".join(str(v) + "LL" for v in reference._LOCAL_RDM_GE[cls]) + "},")
    lines.append("};")
    lines.append("")
    for name, value in sorted(final_constants.items()):
        lines.append(f"constexpr std::int64_t {name} = {value}LL;")
    lines.extend(["", "}  // namespace snn::params", ""])

    output = EXACT / "include" / "locked_parameters.hpp"
    output.write_text("\n".join(lines), encoding="utf-8", newline="\n")
    print(f"generated {output} with {len(constants)} score constants and {len(final_constants)} final constants")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
